        model.addColumn("Cantidad");
        model.addColumn("Precio");
        model.addColumn("Tipo");
        model.addColumn("Imagen");
        
        CtrlProduct ctrl = new CtrlProduct();
        ArrayList<Product> arrayProducts = ctrl.getProducts();
        
        Product product = null;
        String type = "";
              
        for (int i = 0; i < arrayProducts.size(); i++) {
            product = arrayProducts.get(i);
            if (product != null) {
                if (product instanceof Madera) {
                    type = "Madera";
                } else if (product instanceof Metal) {
                    type = "Metal";
                } else {
                    type = "Chapa";
                }
            }
            model.addRow(new Object[]{product.getId(), product.getTitle(), product.getDescription(), product.getStock(), product.getPrice(), type, product.getImgName()});
        }
        
        tblProduct.setModel(model);
        
        tblProduct.setFont(new Font("Tahoma", Font.PLAIN, 14));
        tblProduct.setRowHeight(30);
        
        
        int[] arrCol = {40,280,200,60,60,60,200};        
        Util u = new Util();
        u.tableFmt(tblProduct, arrCol);       
        
