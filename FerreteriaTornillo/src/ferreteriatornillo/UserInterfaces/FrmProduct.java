    private boolean isNumber(String cadena, String type) {
       boolean resultado;
       
       if ( type.equals("Integer") ){

            try {
                Integer.parseInt(cadena);
                resultado = true;
            } catch (NumberFormatException excepcion) {
                resultado = false;
            }
        
       } else {

            try {
                Double.parseDouble(cadena);
                resultado = true;
            } catch (NumberFormatException excepcion) {
                resultado = false;
            }
       }

        return resultado;        
    }
    
    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewActionPerformed
        // TODO add your handling code here:
        
        if ( !textTitle.getText().equals("") && !textDescription.getText().equals("") && !textStock.getText().equals("") && !textPrice.getText().equals("") ) {
            
            if ( isNumber(textStock.getText(), "Integer") && isNumber(textPrice.getText(), "Double") ) {
                DefaultTableModel model = (DefaultTableModel)tblProduct.getModel();        
                int id = model.getRowCount() + 1;
                
                String type = null;
                switch (cmbType.getSelectedIndex()) {
                    case 0:
                        type="Madera";
                        break;
                    case 1:
                        type="Metal";
                        break;
                    case 2:
                        type="Chapa";
                        break;
                }
                
                
                model.addRow(new Object[]{id, textTitle.getText(), textDescription.getText(), textStock.getText(), textPrice.getText(), type, textImage.getText()});
                tblProduct.setModel(model);       

                textTitle.setText("");
                textDescription.setText("");
                textStock.setText("");
                textPrice.setText("");
                textImage.setText("");

                this.edited = true;
            } else {
                JOptionPane.showMessageDialog(null, "La cantidad y el precio deben ser numericos", "Añadir Producto", JOptionPane.INFORMATION_MESSAGE);                                    
            }
        } else {
            JOptionPane.showMessageDialog(null, "Complete los campos para añadir un producto", "Añadir Producto", JOptionPane.INFORMATION_MESSAGE);                        
        }
    }//GEN-LAST:event_btnNewActionPerformed

