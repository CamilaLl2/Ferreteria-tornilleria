    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblProduct.getModel();
        
        if ( this.edited ) {

            ArrayList<Product> arrayProducts = new ArrayList<>();

            String type = "";
            
            for (int i=0; i<model.getRowCount(); i++) {  
                type = model.getValueAt(i, 5).toString();
                switch (type) {
                    case "Madera":
                        arrayProducts.add(new Madera(model.getValueAt(i, 0).toString(), model.getValueAt(i, 1).toString(), model.getValueAt(i, 2).toString(), Integer.parseInt(model.getValueAt(i, 3).toString()), Double.parseDouble(model.getValueAt(i, 4).toString()), model.getValueAt(i, 6).toString()));
                        break;
                    case "Metal":
                        arrayProducts.add(new Metal(model.getValueAt(i, 0).toString(), model.getValueAt(i, 1).toString(), model.getValueAt(i, 2).toString(), Integer.parseInt(model.getValueAt(i, 3).toString()), Double.parseDouble(model.getValueAt(i, 4).toString()), model.getValueAt(i, 6).toString()));
                        break;
                    case "Chapa":
                        arrayProducts.add(new Chapa(model.getValueAt(i, 0).toString(), model.getValueAt(i, 1).toString(), model.getValueAt(i, 2).toString(), Integer.parseInt(model.getValueAt(i, 3).toString()), Double.parseDouble(model.getValueAt(i, 4).toString()), model.getValueAt(i, 6).toString()));
                        break;
                }               
            }

            CtrlProduct ctrl = new CtrlProduct();
            boolean isSaved = ctrl.setProducts(arrayProducts);

            if ( isSaved ) {
                JOptionPane.showMessageDialog(null, "Información guardada con éxito", "Productos", JOptionPane.INFORMATION_MESSAGE);            

                FrmLogin frmlogin = new FrmLogin();
                frmlogin.setVisible(true);
                frmlogin.setLocationRelativeTo(null);     
                this.edited = false;

                this.dispose();

            } else {
                JOptionPane.showMessageDialog(null, "Ha ocurrido un error al guardar los datos", "Productos", JOptionPane.ERROR_MESSAGE);                        
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "No existen Cambios para guardar", "Productos", JOptionPane.ERROR_MESSAGE);                                    
        }
    }//GEN-LAST:event_btnSaveActionPerformed

